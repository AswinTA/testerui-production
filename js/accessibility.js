function showLoadingTemplate() {
    $('.green1').addClass('sd0');
    $('.red').addClass('sd05');
    $('.blue').addClass('sd1');
    $('.yellow').addClass('sd15');
}

function hideLoadingTemplate() {
    $('.green1').removeClass('sd0');
    $('.red').removeClass('sd05');
    $('.blue').removeClass('sd1');
    $('.yellow').removeClass('sd15');
}